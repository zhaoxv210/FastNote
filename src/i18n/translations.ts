export function t(lang: Lang, key: string, params?: Record<string, string | number>): string {
  const val = translations[lang]?.[key] ?? translations.zh[key] ?? key;
  if (!params) return val;
  return val.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
